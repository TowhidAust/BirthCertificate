                        <h3>রেজিস্ট্রার জেনারেলের কার্যালয়ে সবাইকে স্বাগতম</h3>
                        <p>জন্ম সনদ একটি শিশুর অধিকার রক্ষাকবচ। জাতিসংঘের শিশু অধিকার সনদের অনুচ্ছেদ ৭ অনুযায়ী, “শিশুর জন্মগ্রহণের পর জন্ম-নিবন্ধীকরণ করতে হবে । জাতীয়তা অর্জন, নামকরণ এবং পিতামাতার পরিচয় জানবার এবং তাদের হাতে পালিত হবার অধিকার আছে ।” ১৮৭৩ সালের ২রা জুলাই তদানীন্তন বৃটিশ সরকার অবিভক্ত বাংলায় জন্ম নিবন্ধন সংক্রান্ত আইন জারী করে। কালের পরিক্রমায় ১১৮ বৎসরে ভৌগোলিক, রাজনৈতিক ও সেই সঙ্গে আইনের নানান পরিবর্তন সত্ত্বেও দেশের তাবৎ মানুষ জন্ম নিবন্ধনের আওতায় না আসায় ২০০১-২০০৬ সালে ইউনিসেফ-বাংলাদেশ এর সহায়তায় পাইলট প্রকল্পের মাধ্যমে দেশের ২৮টি জেলায় ও ৪টি সিটি কর্পোরেশনে জন্ম নিবন্ধনের কাজ নতুনভাবে আরম্ভ হয়। এরই ধারাবাহিকতায় ১৮৭৩ সালের আইন রদ ও রহিত করে সরকার ২০০৪ সনের ৭ ডিসেম্বর ২৯ নং আইন অর্থাৎ জন্ম ও মৃত্যু নিবন্ধন আইন, ২০০৪ প্রবর্তন করে। আইনটি ২০০৬ সালের ৩ জুলাই হতে কার্যকর হয়। ২০০১-২০০৬ সালের পাইলট প্রকল্পের শেষে প্রকল্পটি জন্ম ও মৃত্যু নিবন্ধন প্রকল্প (২য় পর্যায়) নামে ২০০৭ সালে আরম্ভ হয়ে জুন ২০১২ সালে শেষ হয়। বর্তমানে প্রকল্পটির ৩য় পর্যায়ের কার্যক্রম জুলাই ২০১২ থেকে শুরু হয়ে ২০১৬ সালের জুন মাসে শেষ হয় । প্রকল্পটির আর্থিক সহায়তাকারী সংস্থা ইউনিসেফ-বাংলাদেশ। প্রকল্পটির বিপরীতে মোট বরাদ্দ ১৫৫৭.০০ লক্ষ টাকা; তন্মধ্যে বাংলাদেশ সরকারের অংশ ১২০৭.০০ লক্ষ টাকা (জন্ম নিবন্ধক কার্যালয়সমূহের আদায়কৃত জন্ম নিবন্ধন ফিস হতে ১০৫০ লক্ষ টাকা ম্যানুয়াল বই হতে অনলাইনে এন্ট্রির ব্যয় নির্বাহসহ) ও প্রকল্প সাহায্য ৩৫০.০০ লক্ষ টাকা। সারাদেশে ১৫কোটির অধিক লোকের জন্ম নিবন্ধন সম্পন্ন হয়েছে। বর্তমানে সারাদেশে ১১ টি সিটি কর্পোরেশনের ১২৪টি আঞ্চলিক অফিস , ৩২০ টি পৌরসভা, ৪৫৭১ টি ইউনিয়ন পরিষদ, ১৫টি ক্যান্টনমেন্ট বোর্ড মিলিয়ে ৫০৩০ টি অফিস ও ৫৫টি দূতাবাসসহ মোট ৫০৮৫টি নিবন্ধক অফিসে বর্তমানে সরাসরি ও নিয়মিত যোগাযোগ সমন্বয় করে অনলাইনে জন্ম ও মৃত্যু নিবন্ধনের কার্যক্রম চলছে। Real Time BR Data লিঙ্ক-এ যেয়ে দেশের তাৎক্ষণিক অনলাইন জন্ম নিবন্ধনের সংখ্যা দেখা যাবে। জন্ম ও মৃত্যু নিবন্ধন কার্যক্রম অব্যাহত, গতিশীল এবং দেশের নাগরিকদের জন্ম ও মৃত্যুর একটি স্থায়ী ডাটাবেজ সংরক্ষণ রাখার স্বার্থে সরকার ২০১৩ সালের সেপ্টেম্বর মাসে বিদ্যমান জন্ম ও মৃত্যু নিবন্ধন আইনের সংশোধন করে রেজিস্ট্রার জেনারেল, জন্ম ও মৃত্যু নিবন্ধক অফিস স্থাপন করা হয়েছে।</p>
